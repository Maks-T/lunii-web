<?php

namespace App\Filament\Resources\ProductLabels\Schemas;

use Filament\Forms\Components\TextInput;
use Filament\Schemas\Schema;

class ProductLabelForm
{
    public static function configure(Schema $schema): Schema
    {
        return $schema
            ->components([
                TextInput::make('name')
                    ->required(),
                TextInput::make('code')
                    ->required(),
                TextInput::make('color'),
                TextInput::make('icon'),
            ]);
    }
}
